CREATE DATABASE IF NOT EXISTS parking_system
  DEFAULT CHARACTER SET utf8mb4
  DEFAULT COLLATE utf8mb4_general_ci;

USE parking_system;
SET NAMES utf8mb4;

CREATE TABLE IF NOT EXISTS Card (
  卡号 VARCHAR(30) NOT NULL,
  车牌号 VARCHAR(30) NOT NULL,
  车主姓名 VARCHAR(50) NOT NULL,
  联系电话 VARCHAR(30) NOT NULL,
  车卡状态 VARCHAR(20) NOT NULL DEFAULT '正常',
  创建时间 DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  更新时间 DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (卡号),
  UNIQUE KEY uk_card_plate (车牌号),
  CONSTRAINT chk_card_phone CHECK (联系电话 REGEXP '^[0-9+ -]{6,30}$'),
  CONSTRAINT chk_card_status CHECK (车卡状态 IN ('正常', '挂失', '注销'))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS ParkingSpace (
  车位编号 VARCHAR(30) NOT NULL,
  车位状态 VARCHAR(20) NOT NULL DEFAULT '空闲',
  当前停放车牌 VARCHAR(30) NULL,
  固定车位卡号 VARCHAR(30) NULL,
  创建时间 DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  更新时间 DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (车位编号),
  CONSTRAINT chk_space_status CHECK (车位状态 IN ('空闲', '有车')),
  CONSTRAINT fk_space_fixed_card FOREIGN KEY (固定车位卡号) REFERENCES Card(卡号)
    ON UPDATE CASCADE ON DELETE SET NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS ParkingRecord (
  记录编号 VARCHAR(80) NOT NULL,
  卡号 VARCHAR(30) NOT NULL,
  停放车位编号 VARCHAR(30) NOT NULL,
  入库时间 DATETIME NOT NULL,
  出库时间 DATETIME NULL,
  收费数额 DECIMAL(10,2) NULL,
  PRIMARY KEY (记录编号),
  CONSTRAINT fk_record_card FOREIGN KEY (卡号) REFERENCES Card(卡号)
    ON UPDATE CASCADE ON DELETE RESTRICT,
  CONSTRAINT fk_record_space FOREIGN KEY (停放车位编号) REFERENCES ParkingSpace(车位编号)
    ON UPDATE CASCADE ON DELETE RESTRICT,
  CONSTRAINT chk_record_time CHECK (出库时间 IS NULL OR 出库时间 >= 入库时间),
  CONSTRAINT chk_record_fee CHECK (收费数额 IS NULL OR 收费数额 >= 0)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS AuditLog (
  审计编号 BIGINT NOT NULL AUTO_INCREMENT,
  表名 VARCHAR(50) NOT NULL,
  操作类型 VARCHAR(20) NOT NULL,
  操作时间 DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  操作员 VARCHAR(50) NULL DEFAULT '系统',
  记录内容 TEXT NOT NULL,
  PRIMARY KEY (审计编号),
  CONSTRAINT chk_audit_operation CHECK (操作类型 IN ('INSERT', 'UPDATE', 'DELETE'))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS BackupHistory (
  备份编号 BIGINT NOT NULL AUTO_INCREMENT,
  备份时间 DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  备份文件 VARCHAR(255) NOT NULL,
  备份说明 VARCHAR(255) NULL,
  PRIMARY KEY (备份编号)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

