UPDATE ParkingRecord pr
JOIN ParkingSpace old_space ON old_space.车位编号 = pr.停放车位编号
JOIN ParkingSpace new_space ON new_space.车位编号 = CONCAT(LEFT(old_space.车位编号, 2), RIGHT(old_space.车位编号, 2))
SET pr.停放车位编号 = new_space.车位编号
WHERE old_space.车位编号 REGEXP '^[AB]-0[0-9][0-9]$'
  AND new_space.车位状态 = '空闲';

UPDATE ParkingSpace new_space
JOIN ParkingSpace old_space ON new_space.车位编号 = CONCAT(LEFT(old_space.车位编号, 2), RIGHT(old_space.车位编号, 2))
SET new_space.车位状态 = old_space.车位状态,
    new_space.当前停放车牌 = old_space.当前停放车牌
WHERE old_space.车位编号 REGEXP '^[AB]-0[0-9][0-9]$'
  AND old_space.车位状态 = '有车'
  AND new_space.车位状态 = '空闲';

